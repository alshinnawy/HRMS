    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        int selected = table.getSelectedRow();
        if (selected >= 0)
        {
            flag = 1;
            save.setText("حفظ");
            info.setVisible(true);
            FinancialYears fy = new FinancialYears();
            FinancialYearsHandler fh = new FinancialYearsHandler();
            FinancialYearsPk dpk = new FinancialYearsPk((int) table.getValueAt(selected, 1));
            fy.setFinancialYearId(dpk.getFinancialYearId());
            //  JOptionPane.showMessageDialog(null,table.getValueAt(selected,1));
            try
            {
                fy = fh.getFinancialYearsById(fy.getFinancialYearId());
            } catch (FinancialYearsDaoException ex)
            {
                Logger.getLogger(FinancialYear.class.getName()).log(Level.SEVERE, null, ex);
            }
            yname.setText(fy.getFinancialYearTitle());
            to.setDate(fy.getDateTo());
            from.setDate(fy.getDateFrom());
        } else
        {
            JOptionPane.showMessageDialog(null, "اختر صف ");
        }

    }//GEN-LAST:event_updateActionPerformed
    private void viewAll() throws FinancialYearsDaoException
    {
        FinancialYearsHandler fh = new FinancialYearsHandler();
        DefaultTableModel dtm = (DefaultTableModel) table.getModel();
        dtm.setRowCount(0);
        FinancialYears[] list = fh.getAllFinancialYears();
        if (list != null && list.length > 0)
        {
            for (int i = 0; i < list.length; i++)
            {
                Vector newRow = new Vector();
                newRow.addElement(list[i].getFinancialYearTitle());
                newRow.addElement(list[i].getFinancialYearId());
                dtm.addRow(newRow);
            }
            table.setModel(dtm);
            table.updateUI();
            jScrollPane1.setViewportView(table);

        }
    }

