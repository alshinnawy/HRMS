    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        if(name.getText().length()>0 &&name.getText().length()<=100 &&name.getText().trim() !=null)
        {
           String  institutionNames=name.getText().trim();
             InstitutionsHandler ih=new InstitutionsHandler();
            try {
                 DefaultTableModel dtm =(DefaultTableModel) table.getModel();
                  dtm.setRowCount(0);
               Institutions[] list =ih.getInstituteByName(institutionNames);
              
                if(list !=null &&list.length>0)
                {
                        for(int i=0;i<list.length;i++)
                         {
                        Vector newRow =new Vector();  
                        newRow.addElement(list[i].getInstituteName()); 
                        newRow.addElement(list[i].getInstituteId());  
                         dtm.addRow(newRow);
                          }
                        table.setModel(dtm);
                        table.updateUI();
                        jScrollPane1.setViewportView(table);
                         JOptionPane.showMessageDialog(null, "انتهاء البحث "); 
                        // name.setText("");
                }
                else
                JOptionPane.showMessageDialog(null, "هذالاسم غير موجود ");
                
            } catch (InstitutionsDaoException ex) {
                Logger.getLogger(Institutaions.class.getName()).log(Level.SEVERE, null, ex);
            }
           
        }
    }//GEN-LAST:event_searchActionPerformed

