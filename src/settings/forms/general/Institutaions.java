    public boolean ckeckAllIDIsDigit(String str)
    {
       boolean validID=false;
       for(int i=0;i<str.length();i++)
         {
                if(Character.isDigit(str.charAt(i)))
                  validID=true;
                else 
                {
                    validID=false;
                    break;
                }
             }
    return validID;
    }
