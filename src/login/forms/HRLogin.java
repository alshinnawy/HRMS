// Check if CAPS_LOCK is on then Make a note to user

    private void checkCapslock() {
        int keyCode = KeyEvent.VK_CAPS_LOCK;

        if (keyCode == KeyEvent.VK_CAPS_LOCK && Toolkit.getDefaultToolkit().getLockingKeyState(KeyEvent.VK_CAPS_LOCK)) {

            capsLockMsg.setText(" إنتبه : Caps Lock في حالة تنشيط");
            capsLockMsg.setVisible(true);
        } else {
            capsLockMsg.setText("");
            capsLockMsg.setVisible(false);
        }
    }
