    private void accessByRole(String roleName) throws IOException {
        switch (roleName) {
            case "affiar": {
                comObj.showForm(new AffairParts(), this);

                break;
            }
            case "approval": {

                comObj.showForm(new ApprovalParts(), this);
                break;
            }
            case "admin": {
                comObj.showForm(new MainParts(), this);

            }
            break;
        }
    }

